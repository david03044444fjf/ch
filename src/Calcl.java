import java.util.Scanner;
public class Calcl {
    public int Sum(int a, int b) {
        // System.out.println(a + b);
        return a+b;
    }

    public int Del(int a, int b) {
        //System.out.println(a / b);
        return a/b;
    }

    public int Milt(int a, int b) {
        //System.out.println(a * b);
        return a*b;
    }

    public int Dert(int a, int b) {
        //System.out.println(a - b);
        return a-b;
    }
}
